package strings;

public class CharBooleanExample {

    public static void main(String[] args) {

        // width of 16 (2 bytes)

        char myChar = 'A';
        char unicode = '\u00A9';

        System.out.println(myChar);
        System.out.println(unicode);

        boolean one = true;
        boolean two = false;

        System.out.println(one);
        System.out.println(two);

    }
}
